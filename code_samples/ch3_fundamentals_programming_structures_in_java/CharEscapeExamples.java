package ch3_fundamentals_programming_structures_in_java;

public class CharEscapeExamples {
    static void main() {
        char singleQuote = '\'';
        char doubleQuote = '\"';
        char backslash = '\\';
        char newLine = '\n';
        char tab = '\t';

        System.out.println("Single Quote: " + singleQuote);
        System.out.println("Double Quote: " + doubleQuote);
        System.out.println("Backslash: " + backslash);
        System.out.println("New Line:" + newLine + "This is a new line.");
        System.out.println("Tab:" + tab + "This is tabbed.");
    }
}
