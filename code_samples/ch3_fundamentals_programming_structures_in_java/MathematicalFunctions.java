package ch3_fundamentals_programming_structures_in_java;

public class MathematicalFunctions {
    void main() {
        IO.println(Math.sqrt(9)); // 3.0
        IO.println(Math.pow(2, 3)); // 8.0
        IO.println(Math.abs(-5)); // 5
        IO.println(Math.max(10, 20)); // 20
        IO.println(Math.min(10, 20)); // 10
        IO.println(Math.PI); // 3.141592653589793
        IO.println(Math.E); // 2.718281828459045
        IO.println(Math.TAU); // 6.283185307179586
    }
}
